type Instr = u32;
type Reg = u32;

#[derive(Copy, Clone, Debug)]
pub enum VMInstruction {
    Unreachable,               // panic on hit
    Return(Reg, u32),          // memcpy(result, &r1, c2) + return
    Return8(Reg),              // *result = r1 + return
    Return16(Reg),             // *result = r1 + return
    Return32(Reg),             // *result = r1 + return
    Return64(Reg),             // *result = r1 + return
    ReturnVoid,                // return
    Br(Instr),                 // ip = c
    CondBr(Reg, Instr, Instr), // ip = r1 ? c2 : c3
    RegPtr(Reg, u32),          // r1 = registers + offset
    ConstPtr(Reg, u32),        // r1 = constants + offset
    Gep(Reg, Reg, u32),        // r1 = r2 + c3
    Gep32(Reg, Reg, Reg, u32), // r1 = r2 + r3 * c4
    Gep64(Reg, Reg, Reg, u32), // r1 = r2 + r3 * c4
    CLoad(Reg, u32, u32),      // memcpy(r1, constants + c2, c3)
    CLoad8(Reg, u8),           // r1 = c2
    CLoad16(Reg, u16),         // r1 = c2
    CLoad32(Reg, u32),         // r1 = c2
    CLoad64(Reg, u64),         // r1 = c2
    CLoad128(Reg, u32),        // r1 = *(constants + c2)
    CLoadZero128,              // r1 = 0
    Copy(Reg, Reg, u32),       // memcpy(r1, r2, c3)
    Copy8(Reg, Reg),           // r1 = r2
    Copy16(Reg, Reg),          // r1 = r2
    Copy32(Reg, Reg),          // r1 = r2
    Copy64(Reg, Reg),          // r1 = r2
    Copy128(Reg, Reg),         // r1 = r2
    Load(Reg, Reg, u32, u32),  // memcpy(&r1, r2 + c3, c4)
    Load8(Reg, Reg, u32),      // r1 = *(r2 + r3)
    Load16(Reg, Reg, u32),     // r1 = *(r2 + r3)
    Load32(Reg, Reg, u32),     // r1 = *(r2 + r3)
    Load64(Reg, Reg, u32),     // r1 = *(r2 + r3)
    Load128(Reg, Reg, u32),    // r1 = *(r2 + r3)
}

pub struct VMFunctionArgument {
    pub register: Reg,
    pub size: u32,
}

pub struct VMFunction {
    pub start: Instr,
    pub memory: u32,
    pub arguments: Vec<VMFunctionArgument>,
}

pub struct VMProgram {
    pub functions: Vec<VMFunction>,
    pub code: Vec<VMInstruction>,
}

